                clientes da EDM
                . Na Cidade de Maputo, o valor em 2015 era mais elevado, ascendendo a 95% dos clientes (quase 265 mil
                contadores
                pré-pagos). O número de novas ligações registou uma desaceleração em 2015 e 2016, dado que a EDM não
                tinha
                disponíveis
                novos contadores pré-pagos desde o fim de 2015.</p>

            <p class="uk-text uk-text-justify">A introdução do sistema pré-pago requereu uma significante
                reestruturação de vários sectores da EDM, em particular na
                área comercial e técnica, dada a informatização de processos e a alteração de várias práticas no
                terreno.
                Destaque-se
                aqui a implementação do sistema Credelec Online, primeiro na Cidade de Maputo, em Julho de 2012, seguido
                da
                introdução
                faseada de compras através de canais digitais a partir de Novembro de 2013 (mobile wallet, phone
                banking,
                ATM, online) e
