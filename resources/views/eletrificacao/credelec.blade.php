                <h4 class="uk-heading-bullet uk-text-primary uk-text-bold">Sistema de Pré-pagamento de Electricidade –
                    CREDELEC</h4>

                <p class="uk-dropcap uk-text uk-text-justify">Importa destacar que com a implementação do Sistema
                    CREDELEC
                    em 1994, a EDM reduziu os serviços de leituras e
                    distribuição de facturas, facturação por estimativas, reclamações dos clientes pela facturação
                    indevida
                    devido a falta
                    de leituras, entre outras vantagens.</p>

                <p class="uk-text uk-text-justify">Na época, a EDM encontrava-se numa situação financeira difícil devido
                    a
                    vários factores externos e internos à empresa.
                    Externamente, o país encontrava-se em grandes dificuldades económicas e financeiras, em resultado da
                    conjugação de
                    diversos factores: os legados do colonialismo e do processo de descolonização; as contenções
                    geopolíticas da época (e.g.
                    Guerra Fria e crise do petróleo); os legados da guerra civil (1977-1992); as dificuldades
                    encontradas
                    pelo Governo em
                    implementar as suas estratégias políticas e económicas; e até mesmo intempéries naturais (e.g.
                    ciclones
                    e cheias). Estes
                    factores tiveram diferentes impactos nas actividades da EDM. Internamente, a empresa debatia-se com
                    muitas outras
                    questões: falta de pessoal qualificado; reduzido aprovisionamento de material; dificuldade na
                    estandardização das
                    centrais e rede herdada das várias entidades que deram origem à EDM; elevado índice de perdas
                    não-técnicas e de clientes
                    com dívida prolongada; tarifas de energia desactualizadas; e uma situação financeira muito delicada
                    que
                    impedia
                    investimentos avultados no terreno (The World Bank 1987).</p>

                <p class="uk-text uk-text-justify">Assim, a opção por introduzir o sistema pré-pago visava dar solução a
                    algumas destas situações, nomeadamente reduzir
                    perdas não-técnicas e recuperar custos, com vista a alcançar a sustentabilidade financeira da
                    empresa.
                    Dados relativos a
                    1994, indicam que o nível de perdas (técnicas e não-técnicas) chegava a quase 34% da energia
                    disponibilizada para
                    consumo; a este valor acrescentavam-se problemas de perdas de receitas por via de falhas na
                    facturação,
                    elevando o nível
                    das perdas para cerca de 40% (The World Bank 1996). As perdas não-técnicas (por roubo de energia) ou
                    falhas na
                    facturação (não pagamento ou atrasos no pagamento) pareciam ser transversais a toda a população e
                    arrastavam-se há
                    largos anos. Dados relativos à década de 1980 indicavam que 60% das dívidas eram da responsabilidade
                    de
                    instituições
                    públicas e governamentais (The World Bank 1987). A situação agravava-se com o facto de a EDM
                    necessitar
                    de comprar
                    energia à Eskom para fornecer a região Sul do país, dado que a Hidroeléctrica de Cahora Bassa (HCB)
                    se
                    encontrava fora
                    de serviço em resultado dos ataques sofridos durante a guerra civil. Em resultado das taxas de
                    inflação
                    e de câmbio
                    desfavoráveis que se fazia sentir naquela altura, os custos de aquisição eram pesados para a EDM. A
                    empresa tinha
                    dificuldade em ajustar as tarifas, uma vez que quaisquer modificações requeriam a autorização do
                    Governo
                    (The World Bank
                    1996). Os doadores, tais como o Banco Mundial e a Noruega, reconheciam o desejo da EDM em expandir o
                    acesso à energia
                    para clientes de baixa renda, mas ao mesmo tempo aconselhavam mudanças no sistema tarifário capazes
                    de
                    fazer face aos
                    custos de operação e satisfação da demanda futura (The World Bank 1996; O’Keefe e Torp 1990). Na
                    impossibilidade destas
                    alterações tarifárias, a empresa encontrava-se numa situação financeira difícil, sem cash-flow para
                    financiar
                    investimentos em infraestruturas e com dificuldades na cobertura do serviço da dívida (ibid.).</p>

            </div>
            <p class="uk-text uk-text-justify">A EDM introduziu contadores pré-pagos pela primeira vez em
                Novembro de 1995 com um projecto-piloto na cidade da Matola.
                Foram instalados cerca de 500 contadores a clientes com dívida prolongada na rede de Baixa Tensão (BT).
                Numa
                segunda
                fase do projecto piloto, já em 1996, instalaram-se cerca de 600 contadores pré-pagos no bairro do
                Jardim, em
                Maputo. A
                implementação de contadores pré-pagos foi subsequentemente expandida em Maputo (primeiramente nos
                bairros da
                Maxaquene e
                Mahotas, depois na cidade de cimento, chegando a um total de 5.000 contadores) e para as cidades de
                Nampula
                e Nacala.
                Esta expansão foi parcialmente financiada pelo projecto ‘Urban Household Energy Project’ (UHEP,
                1990-1998)
                liderado pelo
                Banco Mundial com o apoio do Fundo Nórdico de Desenvolvimento (NDF), do Banco Árabe para o
                Desenvolvimento
                Económico de
                África (BADEA), a Agência Sueca para o Desenvolvimento Internacional (SIDA) e as contribuições do
                Governo de
                Moçambique
                e da EDM (The World Bank 1999, 2004). A expansão do sistema a outros centros urbanos foi possível após a
                introdução
                legal da tarifa de pré-pagamento pelo Decreto n.º 59/99, de 21 de Setembro. Inicialmente, a tarifa de
                pré-pagamento
                variava de acordo com a tensão fornecida ao cliente, mas a situação foi uniformizada para uma tarifa
                única
                com o Decreto
                n.º 29/2003, de 23 de Junho. Estes esforços inseriam-se na óptica porque a EDM procurava levar a cabo a
                redução de
                perdas não-técnicas no âmbito do “Projecto Nova Imagem,” que iniciou depois da EDM passar de empresa
                estatal
                para
                empresa pública (Julho de 1995).</p>

            <p class="uk-text uk-text-justify ">A actual estratégia da EDM parece indicar que o sistema
                pré-pago é agora o método a adoptar por defeito para as ligações
                em Baixa Tensão (excluindo Grandes Consumidores BT). Todavia, esta não seria a ideia no início. A
                decisão de
                prosseguir
                com a universalização do Credelec parece ter surgido de forma orgânica e pragmática em resultado da
                experiência no
                terreno. Em particular, os técnicos da EDM aperceberam-se rapidamente que o controlo da facturação seria
                muito mais
                eficiente se todos os residentes numa determinada área utilizassem o pré-pago. Uma das consequências
                muito
                positivas
                desta estratégia de universalização do Credelec acaba por ser o consenso (ou, pelo menos, a não
                existência
                de protesto)
                relativamente a este método de pagamento da energia. Situação bem diferente regista-se na vizinha África
                do
                Sul, onde o
                sistema pré-pago tem sido objecto de protestos intensos por parte da população por ser utilizado de
                forma
                discriminatória, para ligar principalmente as populações de baixa renda, e favorecendo as populações
                mais
                ricas. A
                estratégia da EDM de tentar eliminar todos os contadores pós-pagos para clientes residenciais
                contribuirá
                certamente
                para manter a legitimidade do sistema e evitar a percepção popular de discriminação ou favorecimento
                indevidos.</p>

            <p class="uk-text uk-text-justify">Em 2004, a empresa adopta o nome comercial ‘Credelec’ para o
                sistema pré-pago (EDM 2005), ano em que o número de
                clientes com pré-pago ultrapassa os 86.5 mil, correspondendo a cerca de 30% do número total de clientes
                (EDM
                2014). A
                expansão do Credelec para as restantes áreas do país inicia-se em 2005, chegando a todas as sedes de
